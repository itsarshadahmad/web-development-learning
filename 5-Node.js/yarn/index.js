// * Yarn Commands --> Use CLI section of docs for more details
// yarn -v or --version --> Get version
// yarn init or yarn init -y --> Create package.json
// yarn config set init-license ISC --> Set Defaults
// yarn config get init-license --> Get Defaults
// yarn delete init-license --> Remove Defaults

// yarn add package_name --> Install Packages
// yarn install --> Install all node modules from package.json
// yarn global add package_name --> Add/install global packages
// yarn remove package_name --> Remove packages
// yarn update --> Update packages
// yarn upgrade --> Upgrade packages
// yarn add package_name -D (or --dev) --> install dev dependencies
// yarn global remove package_name --> remove global packages

// yarn list --> list all packages dependencies
// yarn list --depth = 0 (or =1, etc) --> list packages dependencies at specified depth
// yarn import --> Create yarn.lock file
// yarn check --> Verify version matches yarn.lock file